"use strict";

let path = require("path");
let vscodeTest = require("vscode-test");
let { testFilterEnvironmentVariable } = require("./test-support.js");

async function mainAsync() {
  await vscodeTest.runTests({
    extensionDevelopmentPath: path.resolve(__dirname),
    extensionTestsPath: path.resolve(__dirname, "test.js"),
    version: "1.49.0",
    launchArgs: [], // @@@ balete
    extensionTestsEnv: {
      [testFilterEnvironmentVariable]: process.argv[2],
      // HACK(strager): Silence lots of log noise generated by VS Code.
      NODE_NO_WARNINGS: "1",
    },
  });
}

// @@@ dedupe?
function logError(error) {
  if (error && error.stack) {
    console.error("quick-lint-js error:", error.stack);
  } else {
    console.error("quick-lint-js error:", error);
  }
}

mainAsync().catch((error) => {
  logError(error);
  process.exit(1);
});
